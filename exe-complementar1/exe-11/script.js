let contador = 10; // Começamos com o contador em 1.

while (contador >= 1) {
  // A condição: o loop continua enquanto o contador for maior ou igual a 1.
  
  console.log(contador); // Exibe o valor do contador.
  
  contador = contador - 1; // Diminui o contador em 1.

}

console.log("Fim do loop!");